'''Способ предствить объект в виде текста в json'''
# Является валидным JS-кодом
# Типы данных в json:
# string, number, object, array, boolean, null(None)

# string
{ "name":"John" }

# number
{ "age":30 }

# object
{
"employee":{ "name":"John", "age":30, "city":"New York" }
}

# array
{
"employees":[ "John", "Anna", "Peter" ]
}

# boolean
{ "sale":true }

# null
{ "middlename":null }
